    public List<String> getAutores() {
        ListModel<String> modeloListaDeAutores = campoAutores.getModel();
        List<String> listaAutores = new ArrayList();
        for (int i = 0; i < modeloListaDeAutores.getSize(); i++) {
            if (!modeloListaDeAutores.getElementAt(i).equals("")) {
                listaAutores.add(modeloListaDeAutores.getElementAt(i));
            }
        }
        return listaAutores;
    }

    public List<String> getInterpretes() {
        ListModel<String> modeloListaDeInterpretes = campoInterpretes.getModel();
        List<String> listaAutores = new ArrayList();
        for (int i = 0; i < modeloListaDeInterpretes.getSize(); i++) {
            if (!modeloListaDeInterpretes.getElementAt(i).equals("")) {
                listaAutores.add(modeloListaDeInterpretes.getElementAt(i));
            }
        }
        return listaAutores;
    }

