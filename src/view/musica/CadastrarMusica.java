    private void btnAdicionaAutorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionaAutorActionPerformed
        // TODO add your handling code here:
        String autor = recebeAutor.getText();
        if (autor != null && !autor.equals("")) {
            this.modeloListaAutores.addElement(autor);
            campoAutores.setModel(modeloListaAutores);
            recebeAutor.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "O nome do autor não pode ser vazio.");
        }
    }//GEN-LAST:event_btnAdicionaAutorActionPerformed

    private void btnAdicionaInterpreteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionaInterpreteActionPerformed
        // TODO add your handling code here:
        String interprete = recebeInterprete.getText();
        if (interprete != null && !interprete.equals("")) {
            this.modeloListaInterpretes.addElement(interprete);
            campoInterpretes.setModel(modeloListaInterpretes);
            recebeInterprete.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "O nome do interprete não pode ser vazio.");
        }
    }//GEN-LAST:event_btnAdicionaInterpreteActionPerformed

    private void btnRemoverAutorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverAutorActionPerformed
        // TODO add your handling code here:
        int selecionado = campoAutores.getSelectedIndex();

        if (selecionado != -1) {
            String autor = (String) modeloListaAutores.remove(selecionado);
            campoAutores.setModel(modeloListaAutores);
            JOptionPane.showMessageDialog(null, "Autor removido: " + autor);

        } else {
            JOptionPane.showMessageDialog(null, "Selecione o autor que você deseja remover!");
        }
    }//GEN-LAST:event_btnRemoverAutorActionPerformed

