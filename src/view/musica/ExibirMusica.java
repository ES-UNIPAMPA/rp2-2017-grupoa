    private void organizaJtable() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Caminho");
        modelo.addColumn("Titulo");
        modelo.addColumn("Descrição");
        modelo.addColumn("Idioma");
        modelo.addColumn("Genero");
        modelo.addColumn("Autores");
        modelo.addColumn("Interpretes");
        modelo.addColumn("Duração");
        modelo.addColumn("Ano");
        if (listaDeMusicas.isEmpty()) {
            modelo.addRow(new String[]{"", "", "", "", "Sem Músicas"});
        } else {
            for (Object midia : colecao.exibirMidia()) {
                Musica musica = (Musica) midia;
                modelo.addRow(new Object[]{
                    musica.getCaminho(),
                    musica.getTitulo(),
                    musica.getDescricao(),
                    musica.getIdioma(),
                    musica.getGenero(),
                    musica.getAutores(),
                    musica.getInterpretes(),
                    musica.getDuracao(),
                    musica.getAno()
                });
            }
        }
        jtJanelaDeExibicao.setModel(modelo);
    }

