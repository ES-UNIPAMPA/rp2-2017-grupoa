    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jtJanelaDeExibicao.getModel();
        try {
            int Selecionado = jtJanelaDeExibicao.getSelectedRow();
            model.removeRow(Selecionado);
            listaDeMusicas.remove(Selecionado);
            JOptionPane.showMessageDialog(null, "Música removida com sucesso!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_jButton2ActionPerformed


